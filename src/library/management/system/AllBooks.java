    public void setTitleBar(){
        TitleBar tBar = new TitleBar(this);

        tBar.setBackButtonVisible(false);
        titleBar.setLayout(new BorderLayout());
        titleBar.removeAll();
        titleBar.add(tBar, BorderLayout.CENTER);
        titleBar.revalidate();
        titleBar.repaint();
    }
    
            
    public void loadBooks(User user){
                
        booksPanel.removeAll();
        
        ArrayList<Book> books = new BookService().getAllBooks();
        
        int rows = (int) Math.ceil(books.size() / 4.0);
        int totalHeight = rows * 120;
        
        booksScrollPane.getVerticalScrollBar().setUnitIncrement(30);
                
        booksPanel.setMinimumSize(new Dimension(booksScrollPane.getViewport().getWidth() - 2, 0));
        booksPanel.setMaximumSize(new Dimension(booksScrollPane.getViewport().getWidth() - 2, Integer.MAX_VALUE));
        booksPanel.setPreferredSize(new Dimension(booksScrollPane.getViewport().getWidth() - 2, totalHeight));

        for(Book book : books){
            BookCard bookCard = new BookCard(user, book, this, new Color(220, 220, 220));
            booksPanel.add(bookCard);
        }


        booksPanel.revalidate();
        booksPanel.repaint();
                
        }

