    public void loadRegisteredUsers(){
        
        SwingUtilities.invokeLater(() ->{
            
            allUsersScrollPane.getVerticalScrollBar().setUnitIncrement(30);
         
            allUsers.removeAll();
            allUsers.revalidate();
            allUsers.repaint();

            ArrayList<User> rUsers = new UserService().getRegisteredUsers();

            for(User user : rUsers){
                UserCard uCard = new UserCard(user, allUsers, this);
                uCard.setButtonsVisible(false);
                uCard.setCheckBoxVisible(true);
                allUsers.add(uCard);
            }
            
            allUsers.setPreferredSize(new Dimension(allUsersScrollPane.getViewport().getWidth() - 25, rUsers.size()*50));

            allUsers.revalidate();
            allUsers.repaint();           
        });

    }
    
    public void setYears(){
        int currentYear = java.time.LocalDate.now().getYear();

        dueYearCb.removeAllItems();

        dueYearCb.addItem(String.valueOf(currentYear));
        dueYearCb.addItem(String.valueOf(currentYear+1));  
    }
    
    
    public void updateDueDay(){
        int year = Integer.parseInt(dueYearCb.getSelectedItem().toString());
        int month = Integer.parseInt(dueMonthCb.getSelectedItem().toString());

        java.time.YearMonth yearMonth = java.time.YearMonth.of(year, month);
        int daysInMonth = yearMonth.lengthOfMonth();
        
        dueDayCb.removeAllItems();
        
        for(int i = 1; i <= daysInMonth; i++){
            if(i < 10){
                dueDayCb.addItem("0" + String.valueOf(i));
            }
            else{
                dueDayCb.addItem(String.valueOf(i));
            }
        }
    }

