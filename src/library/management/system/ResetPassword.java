    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String password = String.valueOf(password_field.getPassword());
        String confirmPassword = String.valueOf(confirmPassword_field.getPassword());
        if(password.equals(confirmPassword)){

            UserResult userResult = new UserService().resetPassword(this.currentUser.getID(), String.valueOf(password), userRoleFlag);
            
            if(userResult.getSuccess()){
                this.currentUser.setChangePassword(userRoleFlag);
                new InformationMessage(userResult.getMessage()).setVisible(true);
            
                if(locationFlag){
                    Dashboard dashboard = new Dashboard(this.currentUser);
                    dashboard.setLocationRelativeTo(null);
                    dashboard.setVisible(true);
                }
                this.dispose();  
            }
            else{
                new InformationMessage("Error: " + userResult.getMessage()).setVisible(true);
            }
        }
        else{
            new InformationMessage("Passwords do not match.").setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

