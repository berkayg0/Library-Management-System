    private void login_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_login_btnActionPerformed
        

    UserResult result = new UserService().login(username_field.getText(), String.valueOf(password_field.getPassword()));

        if(result.getSuccess()){

            this.currentUser = result.getUser();
            
            if(this.currentUser.getChangePassword()){
                ResetPassword resetPassword = new ResetPassword(this.currentUser);
                resetPassword.setLocationRelativeTo(null);
                resetPassword.locationFlag = true;
                resetPassword.setVisible(true);
                this.dispose();
                return;
            }

            Dashboard dashboard = new Dashboard(this.currentUser);
            dashboard.setLocationRelativeTo(null);
            dashboard.setVisible(true);
            this.dispose();  
           
        }
        else{
            message_label.setText(result.getMessage());
            message_label.setForeground(new Color(204,0,51)); 
            message_label.setVisible(true);
        }
    }//GEN-LAST:event_login_btnActionPerformed

