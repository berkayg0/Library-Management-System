    private void addBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBookActionPerformed

        if(title_field.getText().trim().isEmpty() || 
            author_field.getText().trim().isEmpty() || 
            category_field.getText().trim().isEmpty() ||
            quantity_field.getText().trim().isEmpty() ||
            pageCount_field.getText().trim().isEmpty() ||
            year_field.getText().isEmpty()) {
             new InformationMessage("Please fill in all text fields.").setVisible(true);
             return; 
         }
        
        try{
            
            BookService bookService = new BookService();
            
            BookResult existResult = bookService.checkAndUpdateQuantity(title_field.getText(), Integer.parseInt(quantity_field.getText()));

           if(existResult.getSuccess()){
               new InformationMessage(existResult.getMessage()).setVisible(true);
               clearFields();
           }
           else{
               BookResult addResult = bookService.addBook(title_field.getText(), author_field.getText(), category_field.getText(),Integer.parseInt(quantity_field.getText()), Integer.parseInt(pageCount_field.getText()), Integer.parseInt(year_field.getText()));
               new InformationMessage(addResult.getMessage()).setVisible(true);
               if(addResult.getSuccess()){
                   clearFields();
               }
           }            
        }
        catch(NumberFormatException e){
            new InformationMessage("Please enter valid numbers for Quantity, Page Count, and Year.").setVisible(true);           
        }
        catch(Exception e){
            new InformationMessage(e.getMessage()).setVisible(true);   
        }   
        
    }//GEN-LAST:event_addBookActionPerformed

