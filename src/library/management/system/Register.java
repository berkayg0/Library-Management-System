    public void setTitleBar(boolean backBtn){
        TitleBar tBar = new TitleBar(this);

        tBar.setBackButtonAction(e -> goBack());
        tBar.setBackButtonVisible(backBtn);
        titleBar.setLayout(new BorderLayout());
        titleBar.removeAll();
        titleBar.add(tBar, BorderLayout.CENTER);
        titleBar.revalidate();
        titleBar.repaint();
    }    
    
    public void goBack(){
        if (this.previousPage != null) {
            this.previousPage.setVisible(true);
            this.dispose();
        }
    }

