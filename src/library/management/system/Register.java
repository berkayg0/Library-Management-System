    private void register_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_register_btnActionPerformed

        UserResult result = new UserService().register(firstName_field.getText(), lastName_field.getText(), username_field.getText(), email_field.getText(), phone_field.getText(), String.valueOf(password_field.getPassword()));
        
        if(result.getSuccess()){
            Login login = new Login();
            login.setLocationRelativeTo(null);
            login.setVisible(true);
            this.dispose(); 
        }       
        else{
            message_label.setText(result.getMessage());
            message_label.setForeground(new Color(204,0,51)); 
            message_label.setVisible(true);
        }
        
        
    }//GEN-LAST:event_register_btnActionPerformed

