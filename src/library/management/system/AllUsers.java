    @Override
    public void onUserClick(User user) {
        UserInfo userInfo = new UserInfo(user);
        userInfo.setOnLoadPendingUsersCallback(() -> loadPendingUsers());
        userInfo.setOnLoadRegisteredUsersCallback(() -> loadRegisteredUsers());
        userInfo.setVisible(true);
        userInfo.setLocationRelativeTo(null);
    }

    @Override
    public void onUserAccept(User user) {
        userService.approveUser(user.getID());
        loadPendingUsers();
        loadRegisteredUsers();
    }

    @Override
    public void onUserDecline(User user) {
        userService.deleteUser(user.getID());
        loadPendingUsers();
    }
