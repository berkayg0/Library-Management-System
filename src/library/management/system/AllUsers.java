    public void loadRegisteredUsers(){              
        SwingUtilities.invokeLater(() ->{
        registeredUsers.removeAll();
        
        registeredUsersScrollPane.getVerticalScrollBar().setUnitIncrement(30);

        ArrayList<User> rUsers = userService.getRegisteredUsers();

        for(User user : rUsers){
                UserCard uCard = new UserCard(user, registeredUsers, this);
                uCard.setButtonsVisible(false);
                uCard.setCheckBoxVisible(false);
                registeredUsers.add(uCard);                       
        }
        
        registeredUsers.setPreferredSize(new Dimension(registeredUsersScrollPane.getViewport().getWidth() - 20, rUsers.size()*50));
        registeredUsers.revalidate();
        registeredUsers.repaint();            
        });        
    }
    
        public void loadPendingUsers(){
        
            SwingUtilities.invokeLater(() -> {
            pendingUsers.removeAll();
            
            pendingUsersScrollPane.getVerticalScrollBar().setUnitIncrement(30);

            UserDAO userDAO = new UserDAO();

            ArrayList<User> pUsers = userService.getPendingUsers();
            
            for(User user : pUsers){
                UserCard uCard = new UserCard(user, pendingUsers, this);
                 uCard.setCheckBoxVisible(false);
                pendingUsers.add(uCard);
            }

            pendingUsers.setPreferredSize(new Dimension(pendingUsersScrollPane.getViewport().getWidth() - 20, pUsers.size()*50));
            
            pendingUsers.revalidate();
            pendingUsers.repaint();                
            });
            
    }


