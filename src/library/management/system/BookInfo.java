    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed

        
        BookResult result = bookService.updateBook(title_field.getText(), author_field.getText(), category.getText(), Integer.parseInt(quantity_field.getText()), Integer.parseInt(pageCount_field.getText()), Integer.parseInt(year_field.getText()), this.book.getID());
        
        new InformationMessage(result.getMessage()).setVisible(true);
        
        if(result.getSuccess()){
            this.dispose();
        }
        
        if (onUpdateCallback != null) {
            onUpdateCallback.run();
        }
        
    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed

        
        QuestionFrame qFrame = new QuestionFrame(this, "Are you sure you want to delete the book?");

        qFrame.setVisible(true);  

        boolean answer = qFrame.getAnswer(); 
        
        if(answer){
            BookResult result = bookService.deleteBook(this.book.getID());
            
            new InformationMessage(result.getMessage()).setVisible(true);
            
            if (onUpdateCallback != null) {
                onUpdateCallback.run();
            }
        }
        else{
            qFrame.dispose();
        }
       
    }//GEN-LAST:event_deleteActionPerformed

